/*
 * this class is used to define the point object
 * the whole exercise is based on two dimensional points distances
 */
public class Point {
	public double x, y;

	public Point() {
		this.x = 0;
		this.y = 0;
	}

	public Point(double x, double y) {
		this.x = x;
		this.y = y;
	}

}
